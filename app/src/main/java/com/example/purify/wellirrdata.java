package com.example.purify;


public class wellirrdata {

    private int irrWellImg;
    private String irrDesc;

    public wellirrdata(int irrWellImg, String irrDesc) {
        this.irrWellImg = irrWellImg;
        this.irrDesc = irrDesc;
    }

    public int getIrrWellImg() {
        return irrWellImg;
    }

    public void setIrrWellImg(int irrWellImg) {
        this.irrWellImg = irrWellImg;
    }

    public String getIrrDesc() {
        return irrDesc;
    }

    public void setIrrDesc(String irrDesc) {
        this.irrDesc = irrDesc;
    }



}
